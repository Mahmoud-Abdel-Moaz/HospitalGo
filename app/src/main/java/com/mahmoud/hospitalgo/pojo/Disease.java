package com.mahmoud.hospitalgo.pojo;

public class Disease {
    String  name,info;

    public Disease() {
    }

    public Disease(String name, String info) {
        this.name = name;
        this.info = info;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
